public class Q4 {

    //4 Java Reminder - For Loops and ifs
    //Write a Java program which displays all the even numbers (numbers which are divided exactly
    //by 2) in the range between 0 and 100. You should use a for loop for this exercise.


    public static void main(String[] args) {
        for (int i = 0; i<101;i++){
            if (i%2==0){
                System.out.println(i);
            }

        }
    }

}
